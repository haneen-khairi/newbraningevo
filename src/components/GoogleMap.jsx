export default function GoogleMapComponent({ children, ...props }) {
  const { isLoaded } = useJsApiLoader({
    id: "google-map",
    googleMapsApiKey: import.meta.env.VITE_GOOGLE_API_KEY,
    libraries:["drawing"]
  });
  if (!isLoaded) return null;
  return (
    <GoogleMap
      mapContainerStyle={containerStyle}
      zoom={10}
      options={{
        styles: mapStyle,
      }}
      {...props}
    >
      {isLoaded && children}
    </GoogleMap>
  );
}
